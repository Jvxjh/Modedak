Bitumen Dak Leggen

Bitumen – in de volksmond ook wel dakleer genoemd – is al decennialang een van de meest gekozen en betrouwbare materialen voor platte en licht hellende daken. Of het nu gaat om een aanbouw, garage, dakkapel of een groot bedrijfspand in de regio Zwolle; een vakkundig gelegd bitumen dak biedt een ijzersterke bescherming tegen alle Nederlandse weersinvloeden.


De voordelen van een bitumen dak

Het kiezen voor bitumen brengt grote voordelen met zich mee op het gebied van duurzaamheid en prijs-kwaliteitverhouding:

* **100% Waterdicht:** Door de brandmethode smelten de overlappingen van de banen volledig samen. Dit zorgt voor een naadloze en waterdichte barrière.
* **Lange levensduur:** Modern gemodificeerd bitumen (zoals SBS of APP) is elastisch en UV-bestendig. Hierdoor gaat het dak gemiddeld **20 tot 25 jaar** mee.
* **Eenvoudig te repareren:** Mocht er na verloop van tijd stormschade of een lekkage ontstaan, dan is bitumen lokaal zeer snel en efficiënt te herstellen zonder dat het hele dak vervangen hoeft te worden.
* **Uitstekende prijs-kwaliteitverhouding:** Het is een relatief voordelig materiaal in aanschaf en installatie vergeleken met andere dakbedekkingen, terwijl het maximale bescherming biedt.



Hoe er te werk wordt gegaan: Het stappenplan

Vakmanschap staat voorop bij de montage. Om de kwaliteit en de lange levensduur te garanderen, wordt er altijd gewerkt volgens een vast en beproefd proces:

1. Inspectie en voorbereiding

Voordat er gestart wordt, vindt er een grondige inspectie van de onderconstructie (het dakbeschot) plaats. De ondergrond moet volledig bezemvchoon, droog en vetvrij zijn. Eventuele oude, rotte delen worden direct in overleg vervangen.

2. Aanbrengen van de onderlaag

Er wordt eerst een geschikte onderlaag mechanisch bevestigd (gespijkerd) of los gelegd. Deze laag fungeert als extra buffer, vangt de werking van het gebouw op en zorgt ervoor dat de toplaag later perfect hecht.

3. De bitumen toplaag branden

Met behulp van een professionele gasbrander wordt de toplaag vloeibaar gemaakt en gecontroleerd uitgerold. Hierbij wordt exact de juiste temperatuur gehanteerd, zodat het bitumen optimaal samensmelt met de onderlaag en de naden (met een overlap van minimaal 10 centimeter) volledig waterdicht dichtvloeien.

4. Afwerking van de dakranden en details

De randen worden strak afgewerkt met zinken of aluminium daktrimmen om opwaaiing door storm te voorkomen. Ook cruciale details, zoals de hemelwaterafvoer (de aflooppijp), worden met uiterste precisie ingewerkt zodat er nooit water kan blijven staan.

> ** Professionele tip:** Het periodiek reinigen van de dakgoten en het vrijhouden van de afvoeren verlengt de levensduur van het bitumen dak aanzienlijk. Dit kan direct worden meegenomen in het periodieke onderhoudsplan.
